//Sentencias Switch

let nota = 5;

switch (nota){
    case 5:
        console.log("Buen trabajo, sobresaliente") ;
        break;
    case 4:
        console.log("Buen trabajo, pero podrás haberlo hecho mejor")  ;
        break;
    case 3:
        console.log("Has obtenido un suficiente") ;  
        break;
    case 2:
        console.log("No has aprobado por poco") ;
        break;
    case 1:
        console.log("No has hecho nada, trabaja un poquito más para la proxima")  ;
        break;
    default:
        console.log("Error")  ;
        break;

}

